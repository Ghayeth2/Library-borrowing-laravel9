    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $data = Messages::find($id);
        $data->status = 'Read';
        $data->save();
        return view('Admin.message.show',[
            'data'=> $data
        ]);
    }

